def GetChar(M) -> str:
    match M:
        case [1,1,1,1,1]: return "A"
        case [1,1,1,1,-1]: return "B"
        case [1,1,1,-1,1]: return "C"
        case [1,1,1,-1,-1]: return "D"
        case [1,1,-1,1,1]: return "E"
        case [1,1,-1,1,-1]: return "F"
        case [1,1,-1,-1,1]: return "G"
        case [1,1,-1,-1,-1]: return "H"
        case [1,-1,1,1,1]: return "I"
        case [1,-1,1,1,-1]: return "J"
        case [1,-1,1,-1,1]: return "K"
        case [1,-1,1,-1,-1]: return "L"
        case [1,-1,-1,1,1]: return "M"
        case [1,-1,-1,1,-1]: return "N"
        case [1,-1,-1,-1,1]: return "O"
        case [1,-1,-1,-1,-1]: return "P"
        case [-1,1,1,1,1]: return "Q"
        case [-1,1,1,1,-1]: return "R"
        case [-1,1,1,-1,1]: return "S"
        case [-1,1,1,-1,-1]: return "T"
        case [-1,1,-1,1,1]: return "U"
        case [-1,1,-1,1,-1]: return "V"
        case [-1,1,-1,-1,1]: return "W"
        case [-1,1,-1,-1,-1]: return "X"
        case [-1,-1,1,1,1]: return "Y"
        case [-1,-1,1,1,-1]: return "Z"
        case [-1,-1,1,-1,1]: return " "
        case [-1,-1,1,-1,-1]: return ","
        case [-1,-1,-1,1,1]: return "."
        case [-1,-1,-1,1,-1]: return "'"
        case [-1,-1,-1,-1,1]: return "?"
        case [-1,-1,-1,-1,-1]: return "!"